<h3>History</h3>
<p>
  Begun on October 9th 2010, Win XT was written for the love of Greg Perry, since DOpus was no longer an option. 
  Again in 2013, the shortcuts folder was becoming too large to manage and thinking that a search-as-you-type feature 
  which included sub-folders of the match would be great to add, it became logical to resurrect it.
