<h3>History</h3>
<p>
  Begun on December 20th 2000, IViewer in its previous incarnation was built in VB6 and handled pictures, text, 
  pdfs and html files apart from music. It had quite a lot of features that havent yet made it to the new .net version
</p>

<p>
  The .Net version was begun in 2006 with simple features like search-as-you-type. In 2012 the lyrics viewer was added 
  and the trainer stubbed. It was migrated from VB to C# on its 12th anniversary to a more cohesive and easily 
  maintainable codebase.
</p>
