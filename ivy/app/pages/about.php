<h2>History</h2>
<h3>Forming Years</h3>
<p>
  Ivy Technologies was founded in 2000 when Imran and Sriram 
  <a href="http://angelfire.com/rock3/ivy">first began</a> writing windows applications in VB6. 
  Dubbing themselves tekkies for their love of programming and Star Trek, the first years saw the development of
  <ul>
    <li>An alternate file system</li>
    <li>A file encryption program</li>
    <li>A browser</li>
    <li>A library management system</li>
    <li><a href="iviewer">IViewer</a> which continued to haunt Imran till today</li>
    <li>An Electronic Cad POC for Layouts with footprint libraries</li>
    <li>Independant versions of lyrics libraries (Lyriks and Stormbringer)</li>
    <li>A provocative employment database for Empex, Canada, built in a record 3 weeks</li>
    <li>Personal Information managers, College / Inventory Management software and Biorhythm viewers</li>
  </ul>
  You can see other apps developed by Imran in the Software pdf on his 
  <a href="http://cselian.com/imran/profiles/">portfolio profile page</a>.
</p>
<p>
  Then it was disbanded in 2005 cause Imran and <a href="http://sriramkrishnan.com">Sriram</a> graduated, 
  Imran was <a href="http://cselian.com/imran/home">learning html / css</a> whilst moonlighting at Gani &amp; Sons,
  Sriram began working at Microsoft, Hyderabad and 
  <a href="http://lailaborrie.com">Laila</a> moved to Holland.
</p>
<h3>Rebirth</h3>
<p>
  In 2006 began the transition to .Net apps with Imran running solo. IViewer was rewritten in VB.net, 
  FTP Sync and QGen were written out of pure necessity.
</p>
<p>
  Imran collaborated with 
  <a href="mailto:balaji.kannappan@gmail.com">Balaji</a> on a TFS 2005 Addin for automatic deployment of websites
  including script execution (the forerunner of the modern automated deployment process)
</p>
<p>
  2010 saw the creation of a winforms framework for business apps, powered by NHibernate where the 
  screens are built at runtime. The project was abandoned for want of a good BA. It should help spawn a hybrid
  web / windows app, integrating elements of a plain ado.net-enhanced ORM framework.
</p>
<p>
  In 2012 frustration with building playlists in the otherwise flawless KMP caused IViewer development to be re-kindled.
  The decision to migrate to C#, have a 12th anniversary IViewer pan-global launch and put the mettle to the test was finally made.
  Delayed a few months for want of an <a href="updater">updater</a>, a once-over by a good BA (now they're a dime a dozen), 
  and creation of this website, Ivy and IViewer are finally ready for launch.
</p>

