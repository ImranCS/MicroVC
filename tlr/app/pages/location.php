  <p class="head head-b">Getting There</p>
  <div>
    <strong>From Bangalore</strong> to <strong>Mysore (140 kms)</strong> - It is an excellent 4 lane road. 
    Leave early in the morning to avoid the infamous Bangalore Traffic. On the Mysore Ring Road turn right at
    Columbia Asia Hospital and head towards Hunsur Road and Yelwal. This Mysore bypass will save you both time
    and distance. From Yelwal drive straight to Hunsur, Periyapatna and Kushalnagar (90kms).
    <br /> <br />
    Alternatively you could come on <strong>Tumkur Road</strong> (NH 4) turn left at Nelamangala onto the
    Mangalore highway (NH 48) pass Kunigal and turn left at Channarayapatna. Turn right after 2 kms to
    Holenarsipur (about 20 kms). Cross the Hassan rail line turn left into Holenarsipur, turn right at the bus
    stand to Arakalgud (about 20 kms). Turn left just before Arakalgud to Ramnathpura, Konnanur (left) to
    Srirangala, Hebbale in Coorg.
  </div>

  <img src="./img/maps/Map-1.jpg" alt="Map1" width="750" height="569" />
  <p>
    <strong>From Kushalnagar</strong> head towards Madikeri. Turn left at <strong>Guddehosur</strong> which is 4 kms
    from Kushalnagar bus stop. You are now on the road to Siddapur. Go 8 kms and you will see a large hall on
    the left and a stone sign saying Hosapatna. On the opposite side you will see a bus stop - turn just before
    the hall and proceed into the village.
    <br /><br />
    There are a few roads to the right, ignore them, keep left, till you reach a house with sparkling white
    walls and two level roof, which make it easy to find <strong>and, of course, it is the last house on the
    right in the village of Hospatna.</strong>
  </p>

